package course.graduation.model;


public class resume {
        private String studentid;
        private String resume;//个人简历
        private String summry;//总结报告
        private String san;//三方协议
        private String zwjd;//自我鉴定

    public String getStudentid() {
        return studentid;
    }

    public void setStudentid(String studentid) {
        this.studentid = studentid;
    }

    public String getResume() {
        return resume;
    }

    public void setResume(String resume) {
        this.resume = resume;
    }

    public String getSummry() {
        return summry;
    }

    public void setSummry(String summry) {
        this.summry = summry;
    }

    public String getSan() {
        return san;
    }

    public void setSan(String san) {
        this.san = san;
    }

    public String getZwjd() {
        return zwjd;
    }

    public void setZwjd(String zwjd) {
        this.zwjd = zwjd;
    }
}
